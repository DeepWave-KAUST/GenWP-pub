# npz2h5.py
"""
Convert generated NPZ wavefield files to HDF5 files for efficient training.

This script converts the finite-difference wavefield data generated by the
dataset-generation workflow from `.npz` format to `.h5` format. The converted
HDF5 files are organized per shot, which makes them convenient for training the
generative wave propagator.

Each output HDF5 file contains:
    - vp:    velocity model used for the simulation
    - snaps: temporally downsampled pressure wavefield snapshots
    - loc_x: source location along the horizontal direction, if available

The conversion is designed to be efficient for large datasets:
    1. Files are grouped by (model_name, data_id), so the velocity model `vp`
       only needs to be read once for all shots belonging to the same model.
    2. Different data groups are processed in parallel using multiple processes.
    3. Different shots within one data group are written in parallel using threads.

This file is useful when the full generated NPZ dataset is too large or too slow
to load repeatedly during training. HDF5 provides a more suitable format for
fast random access and compressed storage.
"""

import os
import time
from concurrent.futures import ProcessPoolExecutor, ThreadPoolExecutor, as_completed
from multiprocessing import cpu_count

import h5py
import numpy as np


# ──────────────────────────────────────────────────────────────
# 1. Process one velocity-model sample and all its shots
# ──────────────────────────────────────────────────────────────

def process_data_group(args):
    """
    Convert all pending shots belonging to one velocity-model sample.

    Parameters
    ----------
    args : tuple
        A tuple containing:
            md : str
                Name of the velocity-model family, e.g., 'Overthrust',
                'SEAMArid', or 'SEGEAGE'.
            data_id : int
                Index of the velocity model within this model family.
            shot_ids : list of int
                Shot indices that should be converted.
            source_dir : str
                Directory containing the input `.npz` files.
            output_dir : str
                Directory where the converted `.h5` files will be saved.

    Returns
    -------
    md : str
        Model-family name.
    data_id : int
        Velocity-model index.
    done : int
        Number of shots successfully converted.
    skipped : int
        Number of shots skipped because the corresponding `.h5` files
        already exist.
    err : int
        Number of shots that failed during conversion.

    Notes
    -----
    The input files are expected to follow the naming convention:
        {md}{data_id}_shot{shot_id}.npz

    The output files are saved as:
        {md}{data_id}_shot{shot_id}.h5

    For each (md, data_id) group, the velocity model `vp` is read only once
    from the first available `.npz` file and then reused for all shots.
    """

    md, data_id, shot_ids, source_dir, output_dir = args

    # ------------------------------------------------------------
    # 1.1 Check which shots still need to be converted
    # ------------------------------------------------------------

    # Skip shots whose output HDF5 files already exist. This allows the script
    # to be safely resumed after interruption.
    pending = [
        s for s in shot_ids
        if not os.path.exists(
            os.path.join(output_dir, f"{md}{data_id}_shot{s}.h5")
        )
    ]

    # If all shots under this data_id have already been converted, return
    # directly without doing any additional I/O.
    if not pending:
        return md, data_id, 0, len(shot_ids), 0  # done, skipped, error

    # ------------------------------------------------------------
    # 1.2 Read the velocity model once for this data group
    # ------------------------------------------------------------

    vp = None

    # All shots from the same data_id use the same velocity model. Therefore,
    # we only need to read `vp` from one available NPZ file.
    for s in pending:
        npz_path = os.path.join(source_dir, f"{md}{data_id}_shot{s}.npz")

        if os.path.exists(npz_path):
            try:
                vp = np.load(npz_path)["vp"]
            except Exception:
                # If this file is corrupted or cannot be read, try the next shot.
                pass
            break

    # ------------------------------------------------------------
    # 1.3 Define the conversion routine for one shot
    # ------------------------------------------------------------

    def write_one_shot(shot_id):
        """
        Convert one `.npz` shot file to one `.h5` file.

        Parameters
        ----------
        shot_id : int
            Shot index.

        Returns
        -------
        status : str
            Conversion status:
                - 'ok'      : conversion succeeded
                - 'missing' : input NPZ file does not exist
                - 'err:...' : conversion failed with an error message
        """

        npz_path = os.path.join(source_dir, f"{md}{data_id}_shot{shot_id}.npz")
        out_path = os.path.join(output_dir, f"{md}{data_id}_shot{shot_id}.h5")

        # Skip if the source NPZ file is missing.
        if not os.path.exists(npz_path):
            return "missing"

        try:
            # Load NPZ data. The expected fields are:
            #   snaps: wavefield snapshots
            #   vp:    velocity model
            #   loc_x: source location, optional
            data = np.load(npz_path)

            # Convert snapshots to float32 to reduce storage and match
            # common deep-learning training precision.
            snaps = data["snaps"].astype(np.float32)

            nt, ngrid = snaps.shape[0], snaps.shape[1]

            # Write one HDF5 file for this shot.
            with h5py.File(out_path, "w") as f:

                # Store the velocity model. Gzip compression is used for `vp`
                # because it is read repeatedly but only stored once per file.
                if vp is not None:
                    f.create_dataset(
                        "vp",
                        data=vp,
                        compression="gzip",
                        compression_opts=1,
                    )

                # Store wavefield snapshots.
                # LZF compression is fast and suitable for training data that
                # need to be read frequently. Chunking along the time dimension
                # improves partial loading of temporal subsets.
                f.create_dataset(
                    "snaps",
                    data=snaps,
                    compression="lzf",
                    chunks=(min(25, nt), ngrid, ngrid),
                    shuffle=True,
                    fletcher32=True,
                )

                # Store source location if it is available in the NPZ file.
                if "loc_x" in data:
                    f.create_dataset("loc_x", data=data["loc_x"])

                # Store metadata as HDF5 attributes. These attributes are useful
                # for debugging, dataset indexing, and reproducibility.
                f.attrs["md"] = md
                f.attrs["data_id"] = data_id
                f.attrs["shot_id"] = shot_id

            return "ok"

        except Exception as e:
            # If writing fails, remove the incomplete HDF5 file to avoid
            # accidentally using a corrupted file during training.
            if os.path.exists(out_path):
                os.remove(out_path)

            return f"err:{e}"

    # ------------------------------------------------------------
    # 1.4 Convert shots in this data group using threads
    # ------------------------------------------------------------

    done = 0
    err = 0

    # Writing multiple HDF5 files is mostly I/O-bound, so thread-level
    # parallelism is useful within each data group.
    with ThreadPoolExecutor(max_workers=len(pending)) as tex:
        for r in tex.map(write_one_shot, pending):
            if r == "ok":
                done += 1
            elif r != "missing":
                err += 1

    skipped = len(shot_ids) - len(pending)

    return md, data_id, done, skipped, err


# ──────────────────────────────────────────────────────────────
# 2. Scan pending files before conversion
# ──────────────────────────────────────────────────────────────

def scan_pending(md, output_dir, ndata, nshot):
    """
    Scan which data groups still have missing HDF5 files.

    Parameters
    ----------
    md : str
        Name of the velocity-model family.
    output_dir : str
        Directory containing or receiving the converted HDF5 files.
    ndata : int
        Number of velocity models in this model family.
    nshot : int
        Number of shots per velocity model.

    Returns
    -------
    pending : list of tuple
        Each entry has the form:
            (data_id, missing_shot_ids)

        where `missing_shot_ids` contains the shots that still need conversion.

    Notes
    -----
    This pre-scan avoids submitting tasks that are already fully completed,
    which is useful when converting a large dataset or resuming an interrupted
    conversion.
    """

    pending = []

    for data_id in range(1, ndata + 1):

        # Find shots whose HDF5 output files are missing.
        missing = [
            s for s in range(1, nshot + 1)
            if not os.path.exists(
                os.path.join(output_dir, f"{md}{data_id}_shot{s}.h5")
            )
        ]

        # Only submit this data_id if at least one shot is missing.
        if missing:
            pending.append((data_id, missing))

    return pending


# ──────────────────────────────────────────────────────────────
# 3. Convert all model families
# ──────────────────────────────────────────────────────────────

def convert_all(
    md_list,
    source_dir,
    output_dir,
    ndata=83,
    nshot=5,
    max_workers=None,
):
    """
    Convert all NPZ files from multiple model families to HDF5 format.

    Parameters
    ----------
    md_list : list of str
        List of velocity-model families to process.
    source_dir : str
        Directory containing input `.npz` files.
    output_dir : str
        Directory where output `.h5` files will be saved.
    ndata : int, optional
        Number of velocity models for each model family.
    nshot : int, optional
        Number of shots simulated for each velocity model.
    max_workers : int or None, optional
        Number of worker processes. If None, the number is set to
        min(cpu_count(), 32).

    Notes
    -----
    The conversion uses two levels of parallelism:

    1. Process-level parallelism:
       Different (model family, data_id) groups are distributed across
       multiple processes.

    2. Thread-level parallelism:
       Within each process, different shots belonging to the same data_id
       are written using multiple threads.

    This design is efficient for a large number of independent shot files.
    """

    os.makedirs(output_dir, exist_ok=True)

    # Choose the number of worker processes automatically if not specified.
    if max_workers is None:
        max_workers = min(cpu_count(), 32)

    # ------------------------------------------------------------
    # 3.1 Pre-scan all model families and collect pending tasks
    # ------------------------------------------------------------

    print("Scanning existing files...")

    all_tasks = []
    total_shots = 0

    for md in md_list:
        pending = scan_pending(md, output_dir, ndata, nshot)

        shots_pending = sum(len(s) for _, s in pending)
        total_shots += shots_pending

        skipped_data = ndata - len(pending)

        print(
            f"  [{md:10s}] data pending={len(pending):3d}/{ndata} "
            f"shots pending={shots_pending:5d}/{ndata * nshot} "
            f"(skipped {skipped_data} fully-done data)"
        )

        # Each task corresponds to one velocity model and all its missing shots.
        for data_id, shot_ids in pending:
            all_tasks.append(
                (md, data_id, shot_ids, source_dir, output_dir)
            )

    print(f"\nTotal tasks (data groups): {len(all_tasks)}")
    print(f"Total shots to write     : {total_shots}")
    print(f"Workers                  : {max_workers}\n")

    if not all_tasks:
        print("Nothing to do, all files exist.")
        return

    # ------------------------------------------------------------
    # 3.2 Launch parallel conversion
    # ------------------------------------------------------------

    stats = {
        md: {
            "done": 0,
            "skip": 0,
            "err": 0,
        }
        for md in md_list
    }

    t0 = time.time()
    total_done = 0

    # Use a process pool to process different data groups in parallel.
    with ProcessPoolExecutor(max_workers=max_workers) as executor:

        futures = {
            executor.submit(process_data_group, t): t
            for t in all_tasks
        }

        for future in as_completed(futures):
            md, data_id, done, skipped, err = future.result()

            stats[md]["done"] += done
            stats[md]["skip"] += skipped
            stats[md]["err"] += err

            total_done += done

            elapsed = time.time() - t0
            speed = total_done / elapsed if elapsed > 0 else 0
            eta = (total_shots - total_done) / speed if speed > 0 else 0

            if err:
                print(f"  [ERR] {md} data{data_id} err={err}")

            # Print progress every 10 data groups, i.e., every nshot * 10 shots.
            if total_done % (nshot * 10) == 0:
                print(
                    f"  shots done={total_done:5d}/{total_shots} | "
                    f"{speed:5.1f} shots/s | ETA {eta:5.0f}s"
                )

    # ------------------------------------------------------------
    # 3.3 Print final summary
    # ------------------------------------------------------------

    elapsed = time.time() - t0

    print(f"\n{'=' * 60}")
    print(
        f"All finished in {elapsed:.1f}s  "
        f"({total_done / elapsed:.1f} shots/s)\n"
    )

    print(f"{'Model':<12} {'done':>6} {'skip':>6} {'err':>5}")
    print("-" * 32)

    for md in md_list:
        s = stats[md]
        print(
            f"  {md:<10} {s['done']:>6} {s['skip']:>6} {s['err']:>5}"
        )


